        ndiv.setAttribute("class", "ndiv");
        var name = document.createElement("p");
        name.setAttribute("class", "name");
        var prdiv = document.createElement("div");
        prdiv.setAttribute("id", "prdiv");
        var cp = document.createElement("span");
        cp.setAttribute("class", "cp");
        var mp = document.createElement("span");
        mp.setAttribute("class", "mp");
        var btn = document.createElement("button");
        btn.setAttribute("class", "cartButton")
        mimg.src = el.img_url;
        mdiv.append(mimg);
        mdiv.addEventListener("click", function(el) {
                window.location.href = './product page/beardo project/product.html';
            })
            // dimg.src = el.discountSticker;
            // ddiv.append(dimg);

        name.innerText = el.name;
        ndiv.append(name);


        cp.innerText = el.cutPrice;
        // console.log(rupee);
        // let rupee = el.price[0];
        let price = el.price;
        // console.log((+price));
        mp.innerText = +price;

        btn.innerText = "ADD TO CART";

        // -----------------------------------------------------add to cart addeventlistener--------------------------------------------------------------------------

        btn.addEventListener("click", function() {

            cart_data(el)

            // alert("Added to cart");

        })

        // console.log(el.img_url, el.name, el.price, el.cutPrice);
        // midv.append(mimg, mdiv, ddiv)
